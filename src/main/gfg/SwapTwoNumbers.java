public class SwapTwoNumbers {
    public static void main(String[] args)
    {
        int a,b,temp;
        a = 10;
        b = 15;
        System.out.println(a);
        System.out.println(b);

        temp = a;
        a =b;
        b =temp;

        System.out.println(a);
        System.out.println(b);


    }
}
